package fr.insarouen.iti.prog.itiaventure.elements;

/**
 * Énumération des états possibles pour une porte.
 */
public enum Etat{
    /**
     * La porte est cassée.
     */
    CASSE,
    /**
     * La porte est déverrouillée.
     */
    DEVEROUILLE,
    /**
     * La porte est fermée.
     */
    FERME,
    /**
     * La porte est ouverte.
     */
    OUVERT,
    /**
     * La porte est verrouillée.
     */
    VERROUILLE
}